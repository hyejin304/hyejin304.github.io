  <div class= "philippines-text2">
    <p>Unlike <a href= http://www.kawasanfalls.net/>Kawasan Fall</a> or <a href= https://www.tripadvisor.com.sg/Attraction_Review-g7351247-d8621881-Reviews-Binalayan_Hidden_Waterfalls-Samboan_Cebu_Island_Visayas.html>the Binalayan Falls</a>, the Tumalog Falls is not widely known among tourists. Thus, it's possible to enjoy the cooling water and beautiful scenery without surrounding yourself around too many tourists.</p>
    <p> The Tumalog fall is located at the very bottom of a steep, mountainous road. While you can walk, it may take a while for you to get there. I strongly recommend renting a car, but if this is not feasible, you can find friendly motorcyclists at the top of the road who would be more than happy to drive you down the slope at a small cost.</p>
  </div>
